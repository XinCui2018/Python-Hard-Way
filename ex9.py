# Here are some strange stuff, remember type it exactly.

# print a string
days = "Mon Tue Wed Thu Fri Sat Sun"
# the content after \n will go to a new line
months = "\nJan\nFeb\nMar\nApr\nMay\nJun\nJul\nAug"

# do not forget to use () when you use print function
print ("Here are the days: ", days)
# Also, do not forget the comma , after the quotation marks or before the variables
print ("Here are the months: ", months)

# """""" three double quotes example
print ("""
There is something going on here.
With the three double-quotes.
We can type as much as we like.
Even 4 lines if we want,
or 5,
or 6.
""")

# use backslash n to make a string go across multiple lines.
years = "\n2011\n2012\n2013\n2014\n2015"
print("Here are the years: ", years)